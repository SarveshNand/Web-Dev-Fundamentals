const month = 3

switch (month) {
    case 1:
        console.log("January");
        break;
    case 2:
        console.log("February");
        break;
    case 3:
        console.log("March");
        break;
    case 4:
        console.log("April");
        break;
    case 5:
        console.log("May");
        break;
    case 6:
        console.log("June");
        break;
    case 7:
        console.log("July");
        break;
    case "aug":
        console.log("August");
        break;
    case "sept":
        console.log("September");
        break;
    case "oct":
        console.log("October");
        break;
    case "nov":
        console.log("November");
        break;
    case "dec":
        console.log("December");
        break;
    default:
        console.log("Default Case Matched");
        break;
}